     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data=$this->validate($request,[
            'order'=>'required',
            'cate_id'=>'required',
            'content'=>'required',
            'title'=>'required',
        ]);

        Article::create($data);
        session()->flash('success','添加文章成功');
        return redirect()->route('article.index');
    }

